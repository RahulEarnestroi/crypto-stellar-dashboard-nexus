// Function to simulate price updates
export function updateMockPrices(data: CryptoTicker[]): CryptoTicker[] {
  return data.map(ticker => ({
    ...ticker,
    price: ticker.price * (1 + (Math.random() - 0.5) * 0.02), // ±1% random change
    change24h: ticker.change24h + (Math.random() - 0.5) * 2, // ±1% change to 24h %
  }));
}